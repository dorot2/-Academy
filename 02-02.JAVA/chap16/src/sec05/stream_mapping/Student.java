package sec05.stream_mapping;

public class Student {

	private String name;
	private int score;
	
	public Student(String name, int score) {
		this.name = name;
		this.score = score;
	}
	
	// Getter 메소드
	public String getName() {
		return name;
	}
	public int getScore() {
		return score;
	}
	
	
	
	
}
